CREATE TABLE DEPARTAMENT (
	ID_DEPARTAMENT INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
	NM_DEOARTAMENT VARCHAR(255) NOT NULL,
	PH_DEPARTAMENT VARCHAR(30) NOT NULL
);
	

CREATE TABLE USER (
	ID_USER INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
	EM_USER VARCHAR(80) NOT NULL,
	PW_USER VARCHAR(80) NOT NULL,
	PH_USER VARCHAR(30) NOT NULL,
	NM_USER VARCHAR(255) NOT NULL,
	DEPARTAMENT_ID_DEPARTAMENT INTEGER NOT NULL,
	FOREIGN KEY (DEPARTAMENT_ID_DEPARTAMENT) REFERENCES DEPARTAMENT(ID_DEPARTAMENT)
);

